package za.ac.cput.projectsss;

public class Calculator {
    //ADDITION
    public int add(int a, int b){
        return a+b;
    }


    //SUBTRACTION
    public int sub(int a, int b){
        return a-b;
    }


    //DIVISION
    public int div( int a, int b){
        return a/b;
    }



    //MULTIPLICATION
    public int multi(int a , int b){
        return a*b;
    }
}
